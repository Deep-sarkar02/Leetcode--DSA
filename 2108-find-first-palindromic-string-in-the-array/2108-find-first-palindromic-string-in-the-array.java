class Solution {
    boolean isPalindrome(String s) {
    int left = 0, right = s.length() - 1;

    while (left < right) {
        if (s.charAt(left) != s.charAt(right))
            return false;
        left++;
        right--;
    }
    return true;
}

    public String firstPalindrome(String[] words) {
        for(int i = 0 ; i <words.length ; i++)
        {
            if(isPalindrome(words[i])) return words[i];
        }
        return "";
    }
}